package Pertemuan16;

public class Customer14 {
    public int id;
    public String nama;

    public Customer14() {

    }

    public Customer14(int id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public String toString() {
        return "ID: " + this.id + ", Nama: " + this.nama;
    }

}
